package com.jitlogic.zico.widgets.client;

import com.google.gwt.core.client.GWT;
import com.google.gwt.resources.client.ClientBundle;
import com.google.gwt.resources.client.CssResource;
import com.google.gwt.resources.client.ImageResource;

public interface WidgetResources extends ClientBundle {

    public static final WidgetResources INSTANCE = GWT.create(WidgetResources.class);

    public static interface FormCss extends CssResource {
        String form();
        String fill();
        String item();
        String line();
        String msgbox();
        String btnline();
    }

    public static interface ToolBarCss extends CssResource {
        String button();
        String buttonDown();
        String buttonDisabled();
        String separator();
    }

    @Source("window-close.png")
    ImageResource windowClose();

    @Source("tab-close.png")
    ImageResource tabClose();

    @Source("WidgetResourcesFormCss.css")
    FormCss formCss();

    @Source("ToolBar.css")
    ToolBarCss toolBarCss();
}
